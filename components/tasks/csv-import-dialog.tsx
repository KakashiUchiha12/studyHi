"use client";

import { useState } from "react";
import { Button } from "@/components/ui/button";
import {
    Dialog,
    DialogContent,
    DialogDescription,
    DialogFooter,
    DialogHeader,
    DialogTitle,
} from "@/components/ui/dialog";
import { Textarea } from "@/components/ui/textarea";
import { Alert, AlertDescription } from "@/components/ui/alert";
import { parseTasksCSV, type ParsedTask } from "@/lib/utils/csv-parser";
import { Loader2, CheckCircle2, AlertTriangle, Info } from "lucide-react";
import { cn } from "@/lib/utils";

interface CSVImportDialogProps {
    open: boolean;
    onOpenChange: (open: boolean) => void;
    onImport: (tasks: ParsedTask[]) => Promise<void>;
}

export function CSVImportDialog({
    open,
    onOpenChange,
    onImport,
}: CSVImportDialogProps) {
    const [csvText, setCsvText] = useState("");
    const [isImporting, setIsImporting] = useState(false);
    const [parseResult, setParseResult] = useState<ReturnType<typeof parseTasksCSV> | null>(null);

    const handleParse = () => {
        if (!csvText.trim()) {
            setParseResult({
                success: false,
                tasks: [],
                errors: ["Please paste CSV text"],
                warnings: [],
            });
            return;
        }

        const result = parseTasksCSV(csvText);
        setParseResult(result);
    };

    const handleImport = async () => {
        if (!parseResult || !parseResult.success || parseResult.tasks.length === 0) {
            return;
        }

        setIsImporting(true);
        try {
            await onImport(parseResult.tasks);
            // Success - close dialog and reset
            setCsvText("");
            setParseResult(null);
            onOpenChange(false);
        } catch (error) {
            console.error("Import failed:", error);
        } finally {
            setIsImporting(false);
        }
    };

    const handleClose = () => {
        if (!isImporting) {
            setCsvText("");
            setParseResult(null);
            onOpenChange(false);
        }
    };

    // Auto-parse when text changes
    const handleTextChange = (value: string) => {
        setCsvText(value);
        // Clear previous parse result when editing
        if (parseResult) {
            setParseResult(null);
        }
    };

    return (
        <Dialog open={open} onOpenChange={handleClose}>
            <DialogContent className="max-w-3xl max-h-[90vh] flex flex-col">
                <DialogHeader>
                    <DialogTitle>Import Tasks from CSV</DialogTitle>
                    <DialogDescription>
                        Paste the CSV text generated by AI to bulk import tasks
                    </DialogDescription>
                </DialogHeader>

                <div className="flex-1 space-y-4 overflow-y-auto">
                    {/* Instructions */}
                    <Alert>
                        <Info className="h-4 w-4" />
                        <AlertDescription>
                            <div className="space-y-1 text-sm">
                                <p className="font-medium">Expected format:</p>
                                <code className="block bg-muted p-2 rounded text-xs overflow-x-auto">
                                    Title,Description,Due Date,Priority,Status,Subject
                                </code>
                                <p className="text-muted-foreground">
                                    Paste the CSV text from your AI chatbot below
                                </p>
                            </div>
                        </AlertDescription>
                    </Alert>

                    {/* CSV Input */}
                    <div className="space-y-2">
                        <label className="text-sm font-medium">CSV Text</label>
                        <Textarea
                            placeholder="Paste your CSV text here..."
                            value={csvText}
                            onChange={(e) => handleTextChange(e.target.value)}
                            className="font-mono text-sm min-h-[200px]"
                            disabled={isImporting}
                        />
                    </div>

                    {/* Parse Button */}
                    {csvText && !parseResult && (
                        <Button onClick={handleParse} variant="outline" className="w-full">
                            Validate CSV
                        </Button>
                    )}

                    {/* Parse Results */}
                    {parseResult && (
                        <div className="space-y-3">
                            {/* Success Summary */}
                            {parseResult.success && parseResult.tasks.length > 0 && (
                                <Alert className="border-green-500 bg-green-50">
                                    <CheckCircle2 className="h-4 w-4 text-green-600" />
                                    <AlertDescription className="text-green-800">
                                        <strong>Ready to import {parseResult.tasks.length} tasks</strong>
                                        <div className="mt-2 text-sm space-y-1">
                                            {parseResult.tasks.slice(0, 3).map((task, idx) => (
                                                <div key={idx} className="truncate">
                                                    • {task.title}
                                                    {task.dueDate && ` (Due: ${task.dueDate.toLocaleDateString()})`}
                                                </div>
                                            ))}
                                            {parseResult.tasks.length > 3 && (
                                                <div className="text-muted-foreground">
                                                    + {parseResult.tasks.length - 3} more tasks...
                                                </div>
                                            )}
                                        </div>
                                    </AlertDescription>
                                </Alert>
                            )}

                            {/* Errors */}
                            {parseResult.errors.length > 0 && (
                                <Alert variant="destructive">
                                    <AlertTriangle className="h-4 w-4" />
                                    <AlertDescription>
                                        <strong>Errors found:</strong>
                                        <ul className="mt-2 space-y-1 text-sm">
                                            {parseResult.errors.map((error, idx) => (
                                                <li key={idx}>• {error}</li>
                                            ))}
                                        </ul>
                                    </AlertDescription>
                                </Alert>
                            )}

                            {/* Warnings */}
                            {parseResult.warnings.length > 0 && (
                                <Alert className="border-yellow-500 bg-yellow-50">
                                    <AlertTriangle className="h-4 w-4 text-yellow-600" />
                                    <AlertDescription className="text-yellow-800">
                                        <strong>Warnings:</strong>
                                        <ul className="mt-2 space-y-1 text-sm">
                                            {parseResult.warnings.slice(0, 5).map((warning, idx) => (
                                                <li key={idx}>• {warning}</li>
                                            ))}
                                            {parseResult.warnings.length > 5 && (
                                                <li className="text-muted-foreground">
                                                    + {parseResult.warnings.length - 5} more warnings...
                                                </li>
                                            )}
                                        </ul>
                                    </AlertDescription>
                                </Alert>
                            )}
                        </div>
                    )}
                </div>

                <DialogFooter>
                    <Button variant="outline" onClick={handleClose} disabled={isImporting}>
                        Cancel
                    </Button>
                    <Button
                        onClick={handleImport}
                        disabled={
                            !parseResult ||
                            !parseResult.success ||
                            parseResult.tasks.length === 0 ||
                            isImporting
                        }
                    >
                        {isImporting && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
                        Import {parseResult?.tasks.length || 0} Tasks
                    </Button>
                </DialogFooter>
            </DialogContent>
        </Dialog>
    );
}
